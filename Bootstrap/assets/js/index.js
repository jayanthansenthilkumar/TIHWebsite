const itemsPerPage = 4; // Change this value to show more or fewer items per page
let currentPage = 1;
let currentBatch = "2024";

function renderTeamMembers() {
  const teamContainer = document.getElementById("team-members");
  teamContainer.innerHTML = "";
  const filteredMembers = teamMembers.filter(
    (member) => currentBatch === "all" || member.batch === currentBatch
  );
  const startIndex = (currentPage - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;
  const membersToDisplay = filteredMembers.slice(startIndex, endIndex);

  membersToDisplay.forEach((member) => {
    const memberDiv = document.createElement("div");
    memberDiv.classList.add("col-lg-6");
    memberDiv.setAttribute("data-aos", "fade-up");
    memberDiv.innerHTML = `
        <div class="team-member d-flex align-items-start">
            <div class="pic"><img src="${member.imgSrc}" class="img-fluid" alt=""></div>
            <div class="member-info">
                <h4>${member.name}</h4>
                <p>${member.dept}</p>
                <div class="social">
                    <a href="${member.social.github}" target="_blank"><i class="bi bi-github"></i></a>
                    <a href="${member.social.linkedin}" target="_blank"><i class="bi bi-linkedin"></i></a>
                </div>
            </div>
        </div>
    `;
    teamContainer.appendChild(memberDiv);
  });

  renderPagination(filteredMembers.length);
}

renderTeamMembers();

function renderPagination(totalItems) {
  const paginationContainer = document.getElementById("pagination-controls");
  paginationContainer.innerHTML = "";
  const totalPages = Math.ceil(totalItems / itemsPerPage);
  for (let i = 1; i <= totalPages; i++) {
    const dot = document.createElement("span");
    dot.classList.add("dot");
    if (i === currentPage) {
      dot.classList.add("active");
    }
    dot.onclick = () => {
      currentPage = i;
      renderTeamMembers();
    };
    paginationContainer.appendChild(dot);
  }
}

function filterBatch(batch) {
  currentBatch = batch;
  currentPage = 1;
  renderTeamMembers();
}

document.addEventListener("DOMContentLoaded", () => {
  const filters = document.querySelectorAll(".team-filters li");
  filters.forEach((filter) => {
    filter.addEventListener("click", () => {
      filters.forEach((f) => f.classList.remove("active"));
      filter.classList.add("active");
      filterBatch(filter.getAttribute("data-filter"));
    });
  });
  renderTeamMembers();
});

// Bubble

const skillsCanvas = document.getElementById("skills-particle-canvas");
const skillsCtx = skillsCanvas.getContext("2d");

let skillsParticlesArray = [];
const skillsNumberOfParticles = 100; // Customize the number of particles for the skills section

skillsCanvas.width = window.innerWidth;
skillsCanvas.height = window.innerHeight;

window.addEventListener("resize", function () {
  skillsCanvas.width = window.innerWidth;
  skillsCanvas.height = window.innerHeight;
  initSkillsParticles();
});

class SkillsParticle {
  constructor(x, y, size, color, velocityX, velocityY) {
    this.x = x;
    this.y = y;
    this.size = size;
    this.color = color;
    this.velocityX = velocityX;
    this.velocityY = velocityY;
  }

  draw() {
    skillsCtx.fillStyle = this.color;
    skillsCtx.beginPath();
    skillsCtx.arc(this.x, this.y, this.size, 0, Math.PI * 2);
    skillsCtx.closePath();
    skillsCtx.fill();
  }

  update() {
    this.x += this.velocityX;
    this.y += this.velocityY;

    // Bounce particles off the edges
    if (this.x + this.size > skillsCanvas.width || this.x - this.size < 0) {
      this.velocityX = -this.velocityX;
    }

    if (this.y + this.size > skillsCanvas.height || this.y - this.size < 0) {
      this.velocityY = -this.velocityY;
    }

    this.draw();
  }
}

function initSkillsParticles() {
  skillsParticlesArray = [];
  for (let i = 0; i < skillsNumberOfParticles; i++) {
    const size = Math.random() * 4 + 1;
    const x = Math.random() * (skillsCanvas.width - size * 2) + size;
    const y = Math.random() * (skillsCanvas.height - size * 2) + size;
    const velocityX = (Math.random() - 0.5) * 2;
    const velocityY = (Math.random() - 0.5) * 2;
    const color = `rgba(${Math.random() * 255}, ${Math.random() * 255}, ${
      Math.random() * 255
    }, 0.7)`;

    skillsParticlesArray.push(
      new SkillsParticle(x, y, size, color, velocityX, velocityY)
    );
  }
}

function animateSkillsParticles() {
  skillsCtx.clearRect(0, 0, skillsCanvas.width, skillsCanvas.height);
  for (let i = 0; i < skillsParticlesArray.length; i++) {
    skillsParticlesArray[i].update();
  }
  requestAnimationFrame(animateSkillsParticles);
}

initSkillsParticles();
animateSkillsParticles();
